; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;FUNCTIONALITY: The functionality of the program is to take in a string value stored at x5002 of the program loaded by a separate string font file
;		and prints out the given string using the two ascii characters at x5000 and x5001 for respective 1 and 0 bits for each character. This
;		is done by iterating through the first line of each character until reaching the null character of the string, and then printing a new
;		line for the following row of the character. Then it is going to iterate through the following line of each respective character in the
;		same order. This process will iterate 16 times, which is the height of all of the characters being printed out.
;
;REGISTER USE:	R0 -- RFF, Output register
;		R1 -- Rasc (Stores ascii value from effective address at FF)
;		R2 -- Rx16 (Stores the product of Rasc [R1] * 16 through the MULT loop)
;		R3 -- Decrements down 16 for MULT loop, also RFD which stores the effective address at FONT_DATA
;		R4 -- RSR (Takes the effective address of the associated FONT_DATA instructions for each given character). R4 alternates values with 
;			"font register" FR in memory in order to store each effective address when cycling through different characters each line and 
;			incrementing through character lines in each line
;		R5 -- R_ROWCOUNT -- Takes the number of rows and increments down every time the null char is encountered at R1. Stores the value at
;			ROWCT and calls the value back in order to prevent semantic error through resetting (infinite loops are not good).
;		R6 -- R_COLCOUNT -- Takes the number of columns in each character before reverting back to the start of the program to print 
;			The respective line of the following character.

.ORIG x3000
LINEST	LD	R0, FF		;	Starting line for both character and row loops -- RFF Starting address of FONT_FILE loaded to R0
	AND	R4, R4, #0	;	Clears R4 to avoid semantic error
	LDR	R1, R0, #2	;	Loading Rasc to R1
	BRz	ENDROW		;	In case that R1 = null
	AND 	R2, R2, #0	;	Clears R2 to avoid semantic error

	LD	R3, SIXT	;	Setting up R3 as multiplication incrementer
MULT	ADD	R2, R2, R1	;	Rx16 <- RASC * 16
	ADD	R3, R3, #-1	;	Incrementing down R3 until it counts down to 0 for multiplication
	BRp	MULT		;	Loops back to repeatedly add Rasc to R2 until R2 stores Rasc * 16

	LEA 	R3, FONT_DATA	; 	RFD	<- FONT_DATA address
	ADD	R4, R2, R3	;	RSR	<- Rx16 + RFD
	LD	R0, FR		;	R0 takes the stored value of the respective row for printing out lines of memory for each character
	ADD	R4, R4, R0	;	R4 	<- RSR + FR Offset
	LD	R5, ROWCT	;	R_ROWCOUNT <- ROWCOUNT (Increments down from 16, see instructions starting at ENDROW for reference)

	LD	R2, FFORIG	;	Replaces R0 with R2 as RFF for the OUT instruction

ROWPR	LD	R6, COLCT	;	R_COLCOUNT 	<- 8 Columns per character
	LDR 	R1, R4, #0 	;	R_SRC		<- Memory at address RSR
COLPR	ADD	R1, R1, #0	;	Buffer for branch instruction
	BRzp	ELSE		;	If R1 is negative, run the next instruction. Else, branch to ELSE.
	
IF	LDR	R0, R2 , #1	;	Setting up R0 for OUT instruction
	OUT			;	Print char at R0
	BRnzp 	CEND		;	Branch to the end of the conditional
ELSE	LDR	R0, R2, #0	;	Loading R0 with RFF
	OUT			;	Print char at R0
CEND	ADD	R6, R6, #-1	;	End of the conditional
	BRnp	LSHIFT		; 	If R_COLCOUNT is not zero, branch to shift R1 logically left
	BRnzp	ENDCHAR		;	Else, branch unconditionally to print the new line at ENDCL

LSHIFT	ADD	R1, R1, R1	; 	Logical Shift Left R1
	BRnzp COLPR

ENDCHAR	LD	R0, FF		;	End behavior when printing eight characters for every column
	ADD	R0, R0, #1	;	R0 reads value of memory at address FF and increments by one
	ST	R0, FF		;	R0 writes value of memory at address FF 
	BRnzp   LINEST		;	Unconditionally branch to the start of the program with data set for the next character for the respecitve line.

ENDROW	LD	R0, N		;	Program end-behavior for each row
	OUT			;	Print New line char
	LD 	R4, FR		; 	Load the incremented R4 from "font register" (FR) in memory
	ADD	R4, R4, #1	;	Increment Row Location on RSR
	ST	R4, FR		;	Store incremented value to FR in memory
	ADD 	R5, R5, #-1	;	Decrement Row Count
	BRz	END		; 	If the row count is zero, branch to halt the program
	ST	R5, ROWCT	;	Stores Decremented row count back at memory labelled ROWCT set for the program at next iteration
	LD	R0, FFORIG	;	Loads the original Font File starting address for the first character in the next row.	
	ST	R0, FF		;	Stores this back into the memory FF to iterate down through the program until this method is reached again.
	BRnp 	LINEST		;	Not done printing rows?
	
END	HALT			;	Halts the program.

 	
FR	.FILL 	x0000		;	"Font Register" memory line -- Stores 
FF	.FILL	x5000		;	Font File starting address
FFORIG	.FILL	x5000		;	Original Font File starting address to call the first character of the string for every row iteration
SIXT	.FILL	x0010		;	Sixteen value for the MULT loop to prevent semantic error from calling modified memory at label ROWCT
ROWCT	.FILL	x0010		;	The count of each row (16)
COLCT	.FILL	x0008		; 	The count of each entry per row (column) (8)
N	.FILL	x000A		;	Reserved newline character in ASCII


